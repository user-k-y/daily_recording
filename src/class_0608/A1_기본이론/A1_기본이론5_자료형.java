package A1_기본이론;

public class A1_기본이론5_자료형 {
	
	public static void main(String[] args) {
		
		//# 자료형(data type)
		//1. 숫자 (정수 : 소수점 X)
		System.out.println(3);
		//1. 숫자 (실수 : 소수점 O)
		System.out.println(3.14);
		//2. 문자 한 개 ''작은 따옴표
		System.out.println('B');//이건 특수한 경우에 쓴다.
		//2. 문자 여러개 ""큰 따옴표
		System.out.println("B");//한 개도 여러개에 포함됨
		//3. 참과 거짓
		System.out.println(true);
		System.out.println(false);
		//보라색 글씨는 예약어다
		//자바 만든 사람 제임스 고슬링 인도네시아산 커피 중 자바라는 이름을 따서 만듦
		//커피를 좋아함
		
		
	}//end of main

}
