package A1_기본이론;

/*
   #  이스케이프 문자  
   		\ 엔터키 바로 위 원표시 역슬레시 (우리가 쓰는 문자가 콘슬러시? 라는 개발자 폰트)
   		\알파벳 -> 기능이 추가된다.
   			\n : 줄바꿈
   			\t : tab 공백 4칸을 지원
   		
 */

public class A1_기본이론9_이스케이프문자 {
	public static void main(String[] args) {
		
		System.out.println("Hello\nJava");
		System.out.println("Hello\tJava");
		
		//\"\'는 ",'을 나타내기 위한 쌍따옴표
		System.out.println("\'\"영희야, 안녕!\"이라고 말해야지\'라고 생각했다.");
		
		
		
		
		
	}// end of main

}
