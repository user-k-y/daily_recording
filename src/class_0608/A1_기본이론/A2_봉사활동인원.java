package A2_연산자_예제LV1;

public class A2_봉사활동인원 {
	public static void main(String[] args) {
		/*
		 주석으로 문제의 내용을 한번씩 정리하면서 풀자
		 반 총 인원 수  = 31
		 봉활 간 남학생 = 3
		 봉활 간 여학생 = 봉활 간 남학생 *3
		 봉활 안 간 학생 = 반 총 인원수 - 봉활 간 남학생 + 봉활 간 여학생
		 봉활 안 간 학생 - 봉활 간 학생
		 반 총 인원수 - (봉활 간 남학생 + 봉활 간 여학생) - (봉활 간 남학생 + 봉활 간 여학생)
		 */
		
		System.out.println(31-(6+18));
		
		
		
		
	}//main

}
