package A1_기본이론;

public class A1_기본이론4_출력문종류 {
	public static void main(String[] args) {
		
		// new line의 약어 : ln
		// ln : new line
		
		// 오류 확인위한 용도로 많이 쓰이고 나중엔 안 쓰인다.
		System.out.println("안녕하세요");
		System.out.print("안녕");
		System.out.println("하세요");
		
		System.out.println(); // 그냥 줄 바꾸는 용도
		
		
	}//end of main

}
